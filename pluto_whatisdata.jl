begin
    I=load("./material/char_kway_teow.jpg");
    X=imresize(I, ratio=1/4);
    (p,q)=size(X);
    A=channelview(X);
    R=Array{Float64}(A[1,:,:]);
	G=Array{Float64}(A[2,:,:]);
	B=Array{Float64}(A[3,:,:]);

	RU, RS, RV=psvd(R);
	GU, GS, GV=psvd(G);
	BU, BS, BV=psvd(B);
	
	rank=60;
	DR=zeros(p,q,rank);
	DG=zeros(p,q,rank);
	DB=zeros(p,q,rank);
    for r=1:rank
	    DR[:,:,r]=sum(RS[n]*RU[1:p,n]*(RV[1:q,n])' for n=1:r);
        DG[:,:,r]=sum(GS[n]*GU[1:p,n]*(GV[1:q,n])' for n=1:r);
        DB[:,:,r]=sum(BS[n]*BU[1:p,n]*(BV[1:q,n])' for n=1:r);
	end
end

